/**
 * 张三
 * 张三的智商
 * 张三打招呼
 * 
 * 车
 * 车撞人
 * 
 * 以往我们分析：收到什么数据，做什么判断，各自该怎么处理
 * 
 * 换个角度：我们把张三看成一个对象
 * 张三有智商 -- 属性
 * 张三打招呼 -- 功能/方法
 * 
 * 车也是一个对象
 * 车能撞人 -- 功能/方法
 *
 */
class Person {
    public $iq = 100;
    
    public function say(){
        $arr = array('早上好', '晚上好', '你妹哈');
        if($this->iq > 100){
            echo $arr[0];
        } else {
            $i = rand(0,2);
            echo $arr[$i];
        }
    }
}

class Car{
    public function hit($perpon){
        $newiq = rand(50, 150);
        $perpon->iq = $newiq;
    }
}

$lisi = new Person();
$qq = new Car();
$lisi->say();
echo '<br>';
$qq->hit($lisi);
echo $lisi->iq.'<br>';
echo '<br>';
$lisi->say();
echo '<br>';
$lisi->say();
echo '<br>';
$lisi->say();
