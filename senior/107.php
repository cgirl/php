/**
 * 面向对象 php5中非常重要的一个概念
 *  
 * 面向对象的哲学
 * 面向对象的概念（类是什么？对象是什么？）
 * 面向对象的基础语法 -- 声明类
 * 如何创建一个对象
 * 
 * 没有面向对象之前，我们能否开发程序
 * 答：当然可以
 * 比如c语言，没有类与对象的概念，被称为面向过程的语言
 * 
 * 面向对象与面向过程，同样是一种编程范式
 * 
 * 回顾以往的代码：每一个步骤，每个分支，都是我们考虑进来，并加以控制，如果步骤/人物角色/分支 特点多的时候
 * 程序庞大，不好维护；而从开发角度讲，无论你多少分支，多少步骤，这1w或2w个步骤，也是在我们的讨论范围内的一个确定情况
 * 
 * 比如一只鸡有可能来吃菜叶，也可能不来，这种情况，就没有必要再你创建之初定义下来
 * 
 * 定义一只鸡，这样的而一个对象，他有一定的智商，有一定的功能至于去不去，受环境的影响，比如菜叶的大小，园子远近
 * 
 * 就是说，作为上帝，创建有功能的对象，对象有一定的功能，对象与对象之间在其功能范围内，相互操作。至于做哪些操作？
 * 比如公鸡可以去吃沙子等。。。
 */

/**
 * 从地址栏接收 gender参数，男/女
